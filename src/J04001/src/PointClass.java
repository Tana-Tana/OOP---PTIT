public class PointClass {
    private double x;
    private double y;

    public PointClass(double y, double x) {
        this.y = y;
        this.x = x;
    }

    public void setY(double y) {
        this.y = y;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
